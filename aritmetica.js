let preco1 = 19.90
let preco2 = 30.50
let total = preco1 + preco2
document.write (`Total = R$ ${total} <br>`)

preco1 = 20
preco2 = 50
total = (preco1 + preco2) - 10
document.write(`Total com desconto = R$ ${total} <br>`)

total = preco1 * 3 + preco2

document.write (`Total com 4 produtos = R$ ${total} <br>`)

total = 5 ** 2 //5X5

document.write (`Resultado de potência 5² = ${total}`)





